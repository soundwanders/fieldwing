/**
 * Normalizes a team name input to match the official CFBD API school name
 * @param input - The team name input to normalize
 * @returns The official CFBD school name or the original input if no match found
 */
export function normalizeTeamName(input: string): string {
	const normalized = input.toLowerCase().trim();
	return TEAM_NAME_MAPPINGS[normalized] || input;
}

/**
 * Gets all possible variations of a team name
 * @param officialName - The official CFBD school name
 * @returns Array of all variations that map to this school
 */
export function getTeamVariations(officialName: string): string[] {
	return Object.entries(TEAM_NAME_MAPPINGS)
		.filter(([_, value]) => value === officialName)
		.map(([key, _]) => key);
}

/**
 * Checks if a given input is a valid team name
 * @param input - The team name to check
 * @returns True if the input maps to a known team
 */
export function isValidTeamName(input: string): boolean {
	const normalized = input.toLowerCase().trim();
	return normalized in TEAM_NAME_MAPPINGS;
}
